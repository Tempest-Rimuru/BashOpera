                            администрации запрещены.
                            Вход в зрительный зал не приветствуется в верхней одежде, с напитками, едой и в нетрезвом
                            состоянии запрещён.
                            В целях безопасности зрителей в помещения театра запрещено проносить оружие, огнеопасные,
                            взрывчатые, ядовитые, пахучие и радиоактивные вещества, колющие и режущие предметы,
                            пиротехнические устройства, лазерные фонарики, наркотические вещества, алкогольные напитки,
                            чемоданы, крупные свертки и сумки, животных. Запрещается находиться в пачкающей одежде или с
