                            Запрещается заходить за установленные ограждения, открывать окна, сидеть и стоять в проходах
                            и на лестницах в зрительных залах, входить в помещения, закрытые для посещения, наносить
                            надписи и расклеивать объявления, плакаты и другую продукцию информационного содержания,
