                            Материальный ущерб, причиненный Театру по вине зрителя, должен быть возмещен в установленном
                            законодательством Российской Федерации порядке.
                            Зритель должен соблюдать форму одежды, соответствующую мероприятию, не являться на
                            мероприятия Театра в шортах, майках и сланцах.
                            При утере жетона или утрате личных вещей из гардероба зритель должен обратиться к работнику
