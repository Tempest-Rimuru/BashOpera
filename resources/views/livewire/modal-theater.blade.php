                        С историей БГТОиБ неразрывно связано имя гениального танцовщика XX века Рудольфа Нуреева. Именно
                        в Уфе, на этой сцене он сделал первые шаги на пути к мировой славе. С 1993 года по инициативе
                        Юрия Григоровича проводятся фестивали балетного искусства им. Р.Нуреева. Авторитет нуреевских
                        торжеств и их известность вышли далеко за пределы республики. В разные годы на сцене БГТОиБ
                        выступали ведущие солисты Большого и Мариинского театров, танцовщики из Бразилии, Аргентины,
                        Германии, Франции, Италии, Испании, Бельгии, Японии, Латвии, США, творческие коллективы
                        «Григорович-балет», «Русский балет» В.Гордеева, Каирский балет, балетная труппа Пермского театра
                        оперы и балета им. П.Чайковского и др.
                        В 1997 году с большим успехом прошли Дни культуры РБ в Москве, в рамках которых были
                        представлены спектакли «Журавлиная песнь» Л.Степанова-З.Исмагилова и «В ночь лунного затмения»
                        С. Низаметдинова. В 2004 году опера З.Исмагилова «Кахым-туря» стала лауреатом Национальной
                        театральной премии «Золотая маска» в номинации «Лучшая работа дирижёра». В 2006 году спектакль
                        «Волшебная флейта» В.-А.Моцарта в постановке У. Шварца был выдвинут на эту премию в трех
                        номинациях, а в 2007 году опера Дж. Верди «Бал-маскарад» претендовала сразу по пяти позициям.
                        В 2008 году «Золотой маски» в номинации «За честь и достоинство» удостоена народная артистка
                        СССР Зайтуна Насретдинова. За выдающийся вклад в культурные достижения Совет международного
                        биографического центра (Кембридж, Великобритания) присудил З. А. Насретдиновой почетное звание
                        «Международный профессионал». В 2005 году редакционной коллегией и творческим советом журнала
                        «Балет» она удостоена приза «Душа танца» в номинации «Мэтр танца». В 2007 году призом «Душа
                        танца» в номинации «Рыцарь танца» награжден заслуженный артист России, народный артист
                        Башкортостана Шамиль Терегулов.
                        В 2006 года театру присуждена Премия Правительства России имени Ф.Волкова в номинации «Лучший
                        творческий коллектив». Она была вручена на VII Международном Волковском фестивале в Ярославле,
                        который открылся балетом «Аркаим» Л.Исмагиловой. В 2007 году в составе делегации мастеров
                        искусств РБ ведущие артисты оперы и балета, симфонический оркестр и хор театра приняли участие в
                        Гала-концерте, посвящённом 450- летию добровольного вхождения Башкирии в состав России (Москва).
                        В 2008 г. в составе делегации мастеров искусств РБ артисты театра выступили в штаб-квартире
                        Юнеско в Париже на выставке-презентации «Башкортостан: 450 лет вместе с Россией». В этом же году
                        симфонический оркестр успешно выступил на гастролях в Южной Корее и был удостоен высокой награды
                        — копии Короны I Корейского императора.
                        Башкирский государственный театр оперы и балета стал победителем X Всероссийского конкурса «1000
                        лучших предприятий и организаций России-2009» в номинации «Лучшее предприятие сферы культуры и
                        искусства России» (Москва). БГТОиБ также был удостоен почетного статуса — «Национальное
                        достояние России — 2010» (Москва).
                        Всё больший авторитет на мировых подмостках завоевывают творческие коллективы театра. Балетная и
                        оперная труппы тепло были приняты на зарубежных гастролях во Франции (1994), США (1995, 1996),
                        Голландии и Бельгии (1995–1996), Турции (1999), Египте (2003, 2005), Португалии (2005, 2006,
                        2007), Таиланде (2006, 2008), Бразилии (2007), Китае (2008), Италии (2003,2009, 2010), Мексике
                        (2009).
                        С небывалым общественным резонансом в масштабе республики и России прошли премьеры спектаклей:
                        «Мадам Баттерфляй» Дж. Пуччини и «Прометей» Р.Сабитова, «Наки» С. Низаметдинова «La marionette»
                        («Марионетка») на музыку С. Стравинского, «Князь Игорь» А. Бородина. В 2009 году открылся Малый
                        зал театра. На новой площадке уже идут новые спектакли и концертные программы: «Любовный
                        напиток» Г. Доницетти, «Вальпургиева ночь» Ш. Гуно, «День рождения Кота Леопольда» Б. Савельева,
                        «Неклассический дивертисмент», музыкальная комедия «Кодаса» («Свояченица») З.Исмагилова Недавно
                        состоялась премьера балета «Семь красавиц» на музыку Кара Караева, которая прошла в рамках XVII
                        Международного фестиваля балетного искусства имени Р. Нуреева.
                        Творческие принципы, формировавшиеся в течение семи десятилетий, живут и развиваются: это
                        бережное отношение к традициям, заложенное предыдущими поколениями, и постоянное
                        совершенствование мастерства. Залог успеха театра — высокопрофессиональные творческие
                        коллективы. Артисты БГТОиБ — лауреаты, дипломанты республиканских, российских и международных
                        конкурсов, обладатели государственных и республиканских премий. По-прежнему коллектив
                        ориентирован на постановку лучших образцов зарубежной и отечественной классики, в сценическом
                        воплощении которых постановщикам и исполнителям удается достичь подлинного мастерства.
